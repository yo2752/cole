 package escrituras.beans;

/**
 * Bean que almacena datos de la tabla TIPO_VIA.
 *
 */
public class Via {

	 private String idVia;
	 private String via;
	 
	public Via() {
		
	}

	public String getIdVia() {
		return idVia;
	}

	public void setIdVia(String idVia) {
		this.idVia = idVia;
	}

	public String getVia() {
		return via;
	}

	public void setVia(String via) {
		this.via = via;
	}	
}

	
